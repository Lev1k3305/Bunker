export function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function pickUnique<T>(arr: T[], count: number): T[] {
  const copy = [...arr];
  const result: T[] = [];
  const n = Math.min(count, copy.length);
  for (let i = 0; i < n; i++) {
    const idx = Math.floor(Math.random() * copy.length);
    result.push(copy[idx]);
    copy.splice(idx, 1);
  }
  return result;
}

export interface PlayerCard {
  id: number;
  name: string;
  profession: string;
  ageGender: string;
  health: string;
  hobby: string;
  phobia: string;
  traitPositive: string;
  traitNegative: string;
  inventory: string;
  extraInfo: string;
}

export function generatePlayers(count: number): PlayerCard[] {
  const professions = pickUnique(PROFESSIONS, count);
  const ageGenders = pickUnique(AGE_GENDER, count);
  const healths = pickUnique(HEALTH, count);
  const hobbies = pickUnique(HOBBIES, count);
  const phobias = pickUnique(PHOBIAS, count);
  const traitsPos = pickUnique(TRAITS_POSITIVE, count);
  const traitsNeg = pickUnique(TRAITS_NEGATIVE, count);
  const inventories = pickUnique(INVENTORY, count);
  const extras = pickUnique(EXTRA_INFO, count);

  const players: PlayerCard[] = [];
  for (let i = 0; i < count; i++) {
    players.push({
      id: i + 1,
      name: `Игрок ${i + 1}`,
      profession: professions[i % professions.length],
      ageGender: ageGenders[i % ageGenders.length],
      health: healths[i % healths.length],
      hobby: hobbies[i % hobbies.length],
      phobia: phobias[i % phobias.length],
      traitPositive: traitsPos[i % traitsPos.length],
      traitNegative: traitsNeg[i % traitsNeg.length],
      inventory: inventories[i % inventories.length],
      extraInfo: extras[i % extras.length],
    });
  }
  return players;
}

